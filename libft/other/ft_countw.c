#include "libft.h"

size_t		ft_countw(char const *s, char c)
{
	size_t			i;
	size_t			j;

	if (!s)
		return (0);
	i = 0;
	j = 0;
	while (s[i])
	{
		if (s[i] != c && (s[i + 1] == c || s[i + 1] == '\0'))
			j++;
		i++;
	}
	return (j);
}
